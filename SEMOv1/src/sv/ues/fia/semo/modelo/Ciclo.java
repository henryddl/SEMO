package sv.ues.fia.semo.modelo;

public class Ciclo {

	private int anio;
	private int ciclo;
	private String estado;
	
	public Ciclo() {
		super();
	}

	public int getAnio() {
		return anio;
	}

	public int getCiclo() {
		return ciclo;
	}

	public void setAnio(int anio) {
		this.anio = anio;
	}

	public void setCiclo(int ciclo) {
		this.ciclo = ciclo;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public String getEstado() {
		return estado;
	}



}
